const express = require('express');
const http = require('http');
const socketIo = require('socket.io');
const mongoose = require('mongoose');
const cors = require('cors');
const rateLimit = require('express-rate-limit');
require('dotenv').config();

const app = express();
const server = http.createServer(app);

// –ù–∞—Å—Ç—Ä–æ–π–∫–∞ Socket.IO
const io = socketIo(server, {
  cors: {
    origin: "*",
    methods: ["GET", "POST"]
  }
});

// Middleware
app.use(cors());
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true }));

// –°—Ç–∞—Ç–∏—á–µ—Å–∫–∏–µ —Ñ–∞–π–ª—ã –¥–ª—è –≤–µ–±-–∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞
app.use(express.static('public'));

// Rate limiting
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 100 // limit each IP to 100 requests per windowMs
});
app.use('/api/', limiter);

// –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ MongoDB Atlas
mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/dechat', {
  useNewUrlParser: true,
  useUnifiedTopology: true,
})
.then(() => console.log('‚úÖ Connected to MongoDB'))
.catch(err => console.error('‚ùå MongoDB connection error:', err));

// –ò–º–ø–æ—Ä—Ç –º–∞—Ä—à—Ä—É—Ç–æ–≤
const authRoutes = require('./routes/auth');
const chatRoutes = require('./routes/chat');

// –ú–∞—Ä—à—Ä—É—Ç—ã API
app.use('/api/auth', authRoutes);
app.use('/api/chat', chatRoutes);

// –ò—Å—Ç–æ—Ä–∏—è 1:1 —Å–æ–æ–±—â–µ–Ω–∏–π (–ø–µ—Ä–µ–Ω–µ—Å–µ–Ω–æ –∏–∑ –∫–æ–Ω—Ü–∞ —Ñ–∞–π–ª–∞)
app.get('/api/direct/messages', async (req, res) => {
  try {
    console.log('üìú GET /api/direct/messages, query:', req.query);
    const { me, peer, limit = 100 } = req.query;
    if (!me || !peer) {
      return res.status(400).json({ error: 'me and peer are required' });
    }
    const DirectMessage = require('./models/DirectMessage');
    const conversationId = [String(me), String(peer)].sort().join('::');
    const items = await DirectMessage.find({ conversationId })
      .sort({ timestamp: 1 })
      .limit(parseInt(limit));
    console.log('üìú Found', items.length, 'messages for', conversationId);
    res.json({ messages: items.map(m => ({ sender: m.sender, recipient: m.recipient, text: m.text, timestamp: m.timestamp })) });
  } catch (err) {
    console.error('Load direct history error:', err);
    res.status(500).json({ error: 'Internal server error' });
  }
});

// –•—Ä–∞–Ω–∏–ª–∏—â–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
const connectedUsers = new Map(); // socketId -> userData
const usernameToSocketId = new Map(); // username -> socketId

// Socket.IO –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏
io.on('connection', (socket) => {
  console.log(`üîå User connected: ${socket.id}`);
  
  // –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø—Ä–∏—Å–æ–µ–¥–∏–Ω–∏–ª—Å—è –∫ —á–∞—Ç—É
  socket.on('user_joined', (data) => {
    console.log(`üë§ User joined: ${data.username}, socketId: ${socket.id}`);
    
    // –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    connectedUsers.set(socket.id, {
      username: data.username,
      online: true,
      socketId: socket.id
    });
    usernameToSocketId.set(data.username, socket.id);
    console.log(`üóÇÔ∏è usernameToSocketId map:`, Array.from(usernameToSocketId.entries()));
    // –ö–æ–º–Ω–∞—Ç–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–Ω–∞ —Å–ª—É—á–∞–π –Ω–µ—Å–∫–æ–ª—å–∫–∏—Ö –≤–∫–ª–∞–¥–æ–∫)
    try { socket.join(`user:${data.username}`); } catch (e) {}
    
    // –£–≤–µ–¥–æ–º–ª—è–µ–º –≤—Å–µ—Ö –æ –Ω–æ–≤–æ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ
    socket.broadcast.emit('user_joined', { username: data.username });
    
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –Ω–æ–≤–æ–º—É –∫–ª–∏–µ–Ω—Ç—É
    const usersList = Array.from(connectedUsers.values());
    socket.emit('users_list', usersList);
    
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –≤—Å–µ–º –æ—Å—Ç–∞–ª—å–Ω—ã–º
    socket.broadcast.emit('users_list', usersList);
  });
  
  // –û—Ç–ø—Ä–∞–≤–∫–∞ –ª–∏—á–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è (1:1)
  socket.on('send_message', async (data) => {
    // –í–µ—Ç–∫–∞ –∫–æ–º–Ω–∞—Ç (—Å—Ç–∞—Ä–æ–≥–æ API) –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç—Å—è –Ω–∏–∂–µ, –∑–¥–µ—Å—å 1:1 –µ—Å–ª–∏ –Ω–µ—Ç chatId
    if (data && !data.chatId && data.sender && data.recipient && data.text) {
      try {
        const payload = {
          sender: data.sender,
          recipient: data.recipient,
          text: data.text,
          timestamp: data.timestamp || Date.now()
        };

        // –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏—Å—Ç–æ—Ä–∏—é
        try {
          const DirectMessage = require('./models/DirectMessage');
          const conversationId = [payload.sender, payload.recipient].sort().join('::');
          await DirectMessage.create({
            conversationId,
            sender: payload.sender,
            recipient: payload.recipient,
            text: payload.text,
            timestamp: payload.timestamp
          });
        } catch (e) {
          console.error('DirectMessage save error:', e.message);
        }

        // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–ª—É—á–∞—Ç–µ–ª—é (–µ—Å–ª–∏ –æ–Ω–ª–∞–π–Ω)
        const recipientSocketId = usernameToSocketId.get(payload.recipient);
        if (recipientSocketId) {
          io.to(recipientSocketId).emit('new_message', payload);
        }

        // –≠—Ö–æ –æ—Ç–ø—Ä–∞–≤–∏—Ç–µ–ª—é (–Ω–µ –¥—É–±–ª–∏—Ä–æ–≤–∞—Ç—å –Ω–∞ —Ñ—Ä–æ–Ω—Ç–µ –≤—Ç–æ—Ä–æ–π —Ä–∞–∑)
        socket.emit('new_message', payload);
        return;
      } catch (err) {
        console.error('Direct send_message error:', err);
        socket.emit('error', { message: 'Failed to send direct message' });
        return;
      }
    }
    // –∏–Ω–∞—á–µ –ø—Ä–æ–¥–æ–ª–∂–∏—Ç –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–Ω–∞—Ç –Ω–∏–∂–µ
  });
  
  // –°—Ç–∞—Ä—ã–µ WebRTC –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —É–¥–∞–ª–µ–Ω—ã - –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ç–æ–ª—å–∫–æ –Ω–æ–≤—É—é —Å–∏—Å—Ç–µ–º—É voice_call_*

  // –ü—Ä–æ—Å—Ç–æ–π –∞–Ω–æ–Ω–∏–º–Ω—ã–π —á–∞—Ç (–¥–ª—è —Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç–∏)
  socket.on('message', (data) => {
    console.log('üì® Message received:', data);
    socket.broadcast.emit('message', {
      type: 'message',
      user: data.user || '–ê–Ω–æ–Ω–∏–º',
      content: data.content,
      timestamp: new Date().toLocaleTimeString()
    });
  });

  socket.on('typing', (data) => {
    socket.broadcast.emit('typing', data);
  });

  // –ü—Ä–∏—Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –∫ –∫–æ–º–Ω–∞—Ç–µ —á–∞—Ç–∞ (–¥–ª—è —Å—Ç–∞—Ä–æ–≥–æ API)
  socket.on('join_chat', (chatId) => {
    socket.join(chatId);
    console.log(`üë• User ${socket.id} joined chat ${chatId}`);
  });

  // –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ –∫–æ–º–Ω–∞—Ç—É —á–∞—Ç–∞ (–¥–ª—è —Å—Ç–∞—Ä–æ–≥–æ API —á–∞—Ç–æ–≤)
  socket.on('send_message', async (data) => {
    // –ï—Å–ª–∏ –Ω–µ—Ç chatId ‚Äî —ç—Ç–æ –Ω–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –∫–æ–º–Ω–∞—Ç—ã, –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º, —á—Ç–æ–±—ã –Ω–µ –º–µ—à–∞—Ç—å 1:1 —Å–æ–æ–±—â–µ–Ω–∏—è–º
    if (!data || !data.chatId) {
      return;
    }
    try {
      const { chatId, encryptedMessage, senderId } = data;

      // –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ –ë–î
      const Message = require('./models/Message');
      const newMessage = new Message({
        chatId,
        senderId,
        encryptedContent: encryptedMessage,
        timestamp: new Date()
      });

      await newMessage.save();

      // –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –≤—Å–µ–º –≤ –∫–æ–º–Ω–∞—Ç–µ
      io.to(chatId).emit('new_message', {
        id: newMessage._id,
        chatId,
        senderId,
        encryptedContent: encryptedMessage,
        timestamp: newMessage.timestamp
      });

    } catch (error) {
      console.error('Error sending message:', error);
      socket.emit('error', { message: 'Failed to send message' });
    }
  });

  // –ì–æ–ª–æ—Å–æ–≤–æ–π –≤—ã–∑–æ–≤ (–º–∞—Ä—à—Ä—É—Ç–∏–∑–∞—Ü–∏—è –ø–æ username -> socketId)
  socket.on('voice_call_request', (data) => {
    try {
      const { targetUserId, callerId, offer } = data || {};
      if (!targetUserId) return;
      const targetSocketId = usernameToSocketId.get(targetUserId) || null;
      console.log('voice_call_request:', { from: callerId, to: targetUserId, targetSocketId });
      if (targetSocketId) {
        io.to(targetSocketId).emit('incoming_call', { callerId, offer });
        console.log(`‚û°Ô∏è incoming_call sent to ${targetUserId} via socketId`);
      } else {
        // –†–µ–∑–µ—Ä–≤: –ø–æ—Å—ã–ª–∞–µ–º –≤ –∫–æ–º–Ω–∞—Ç—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –µ—Å–ª–∏ –Ω–µ—Ç –ø—Ä—è–º–æ–≥–æ socketId
        io.to(`user:${targetUserId}`).emit('incoming_call', { callerId, offer });
        console.log(`‚û°Ô∏è incoming_call sent to ${targetUserId} via room`);
      }
    } catch (e) {
      console.error('voice_call_request error:', e);
    }
  });

  socket.on('voice_call_answer', (data) => {
    try {
      const { callerId, answer } = data || {};
      if (!callerId) return;
      const callerSocketId = usernameToSocketId.get(callerId) || null;
      console.log('voice_call_answer:', { to: callerId, callerSocketId, answer });
      if (callerSocketId) {
        io.to(callerSocketId).emit('call_answered', { answer });
        console.log(`‚û°Ô∏è call_answered sent to ${callerId} via socketId`);
      } else {
        io.to(`user:${callerId}`).emit('call_answered', { answer });
        console.log(`‚û°Ô∏è call_answered sent to ${callerId} via room`);
      }
    } catch (e) {
      console.error('voice_call_answer error:', e);
    }
  });

  socket.on('ice_candidate', (data) => {
    try {
      const { targetUserId, candidate } = data || {};
      if (!targetUserId) return;
      const targetSocketId = usernameToSocketId.get(targetUserId) || null;
      if (targetSocketId) {
        io.to(targetSocketId).emit('ice_candidate', { candidate });
      }
      io.to(`user:${targetUserId}`).emit('ice_candidate', { candidate });
    } catch (e) {
      console.error('ice_candidate error:', e);
    }
  });

  socket.on('disconnect', () => {
    console.log(`üîå User disconnected: ${socket.id}`);
    
    // –£–¥–∞–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ —Å–ø–∏—Å–∫–∞
    const user = connectedUsers.get(socket.id);
    if (user) {
      connectedUsers.delete(socket.id);
      if (user.username) {
        usernameToSocketId.delete(user.username);
      }
      
      // –£–≤–µ–¥–æ–º–ª—è–µ–º –≤—Å–µ—Ö –æ–± —É—Ö–æ–¥–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
      socket.broadcast.emit('user_left', { username: user.username });
      
      // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
      const usersList = Array.from(connectedUsers.values());
      socket.broadcast.emit('users_list', usersList);
      
      console.log(`üë§ User left: ${user.username}`);
    }
  });
});

